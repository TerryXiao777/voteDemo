package com.demo.bean;

public class AreaBean {

    private long areaIpStart;
    private long areaIpEnd;
    private String areaName;
    private int areaBallot;

    public long getAreaIpStart() {
        return areaIpStart;
    }
    public void setAreaIpStart(long areaIpStart) {
        this.areaIpStart = areaIpStart;
    }
    public long getAreaIpEnd() {
        return areaIpEnd;
    }
    public void setAreaIpEnd(long areaIpEnd) {
        this.areaIpEnd = areaIpEnd;
    }
    public String getAreaName() {
        return areaName;
    }
    public void setAreaName(String areaName) {
        this.areaName = areaName;
    }
    public int getAreaBallot() {
        return areaBallot;
    }
    public void setAreaBallot(int areaBallot) {
        this.areaBallot = areaBallot;
    }
}
